DROP TABLE IF EXISTS `tasks` CASCADE;
DROP TABLE IF EXISTS `To_Do_List` CASCADE;

CREATE TABLE `tasks` (
TaskID INTEGER GENERATED BY DEFAULT AS IDENTITY,
name VARCHAR(255) NOT NULL,
description VARCHAR(255),
FK_TODOIDDENTIFICATION INTEGER,
PRIMARY KEY (TaskID)
);

CREATE TABLE `To_Do_List` (
to_DoID INTEGER GENERATED BY DEFAULT AS IDENTITY,
name varchar(255) NOT NULL,
PRIMARY KEY (to_DoID)
);