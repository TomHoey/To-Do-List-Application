DROP TABLE IF EXISTS `tasks` CASCADE;
DROP TABLE IF EXISTS `ToDoList` CASCADE;

CREATE TABLE `tasks` (
TaskID INTEGER GENERATED BY DEFAULT AS IDENTITY,
name VARCHAR(255) NOT NULL,
description VARCHAR(255),
fk_taskID INTEGER,
PRIMARY KEY (TaskID)
);

CREATE TABLE `ToDoList` (
toDoID INTEGER GENERATED BY DEFAULT AS IDENTITY,
name varchar(255) NOT NULL,
PRIMARY KEY (toDoID)
);